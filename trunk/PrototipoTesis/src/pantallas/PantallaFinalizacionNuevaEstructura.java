    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        // TODO add your handling code here:
        GestorGrabarLeerDelDisco control=new GestorGrabarLeerDelDisco();
        if(muebleActual==null)
        {
            muebleActual=new MuebleEstructura();
        }
        
        boolean seEncuentra=false;
        MuebleEstructura aEliminar=null;
        MuebleEstructura aux=new MuebleEstructura();
        LinkedList lista=control.obtenerLista(aux);
        Iterator iter = lista.iterator();
        while(iter.hasNext())
        {
            aux = (MuebleEstructura)iter.next();
            if(aux.getNombre().compareTo(muebleActual.getNombre())==0)
            {
                seEncuentra=true;
                aEliminar=aux;
            }
        }
        if(seEncuentra==true)
        {
            System.out.println("Se elimino el repetido");
            control.eliminarObjetoGrabarLista(lista,aEliminar);
        }
        if(control.grabar(muebleActual)){System.out.println("Se grabo "+muebleActual.getNombre());}
        PantallaInicialMueble pantCM=new PantallaInicialMueble();
        pantCM.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnFinalizarActionPerformed

