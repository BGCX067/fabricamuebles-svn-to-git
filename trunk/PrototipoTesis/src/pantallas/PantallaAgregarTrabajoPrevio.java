    void setMuebleActual(MuebleEstructura mueble) {
        muebleActual=mueble;
        if(muebleActual==null)
        {
            muebleActual=new MuebleEstructura();
        }
        actualizarTablaTrabajosPrevios(muebleActual.getTrabajos());
    }

    private void actualizarTablaTrabajosPrevios(LinkedList<Trabajo> listaTrabajos) {
        String nombresColumnas[] = {"Trabajos Previos"};
        trabajosTablaModel = new DefaultTableModel(nombresColumnas,0);
        Iterator iter = listaTrabajos.iterator();
        while(iter.hasNext())
        {
            Trabajo aux = (Trabajo)iter.next();
            String datosFila[] = {aux.getNombre()};
            trabajosTablaModel.addRow(datosFila);
            
        }
        tablaTrabajosPrevios.setModel(trabajosTablaModel);
    }
    
    private void cargarMouseListener()
    {
        MouseAdapter adaptadorMouse=new MouseAdapter()
           {@Override
           public void mouseClicked(MouseEvent e) 
              {
                 int fila = tablaTrabajosPrevios.rowAtPoint(e.getPoint());
                 int columna = tablaTrabajosPrevios.columnAtPoint(e.getPoint());
                 if ((fila > -1) && (columna > -1))
                 {
                     txtSeleccionado.setText(tablaTrabajosPrevios.getModel().getValueAt(fila, 0)+"");
                 }
              }
           };
        
        tablaTrabajosPrevios.addMouseListener(adaptadorMouse);
    }
