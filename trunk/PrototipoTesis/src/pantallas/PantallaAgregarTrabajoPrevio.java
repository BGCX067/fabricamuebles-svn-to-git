    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(txtSeleccionado.getText().compareTo("")!=0)
        {
            Iterator iter = muebleActual.getTrabajos().iterator();
            Trabajo seleccionado=null;
            while(iter.hasNext())
            {
                Trabajo aux = (Trabajo)iter.next();
                if(aux.getNombre().compareTo(txtSeleccionado.getText())==0)
                {
                    seleccionado=aux;
                }

            }
            PantallaAgregarTrabajo padre=(PantallaAgregarTrabajo) this.getParent();
            padre.agregarNuevoTrabajoPrevio(seleccionado);
        }
        this.dispose();
    }//GEN-LAST:event_btnAgregarActionPerformed

