    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        lblInformacion.setText("");
        
        if(chekearTextFields()==false)
        {
            JOptionPane.showMessageDialog(getContentPane(),"Debe ingresar todos los datos","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String nombre=txtNombrePieza.getText();
            String largo=txtLargo.getText();
            String ancho=txtAncho.getText();
            String espesor=txtEspesor.getText();
            String cantidad=txtCantidad.getText();
            Pieza nueva=new Pieza();
            nueva.setNombre(nombre);
            nueva.setLargoCM(Float.parseFloat(largo));
            nueva.setAnchoCM(Float.parseFloat(ancho));
            nueva.setEspesorMM(Integer.parseInt(espesor));
            nueva.setCantidad(Integer.parseInt(cantidad));
            PantallaPiezas padre=(PantallaPiezas)this.getParent();
            padre.agregarNuevaPieza(nueva);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

