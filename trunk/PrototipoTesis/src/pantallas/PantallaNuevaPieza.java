    private boolean chekearTextFields()
    {
        if(txtNombrePieza.getText().compareTo("")==0)
        {
            return false;
        }
        if(txtLargo.getText().compareTo("")==0)
        {
            return false;
        }
        if(txtAncho.getText().compareTo("")==0)
        {
            return false;
        }
        if(txtEspesor.getText().compareTo("")==0)
        {
            return false;
        }
        if(txtCantidad.getText().compareTo("")==0)
        {
            return false;
        }
        return true;
    }
    
    private void dibujar(int ancho1, int largo, int espesor)
    {
        int alto=espesor/10;
        int ancho=largo;
        int profundidad=ancho1/2;
        
        int valorX[]=new int[4];
        int valorY[]=new int[4];
        
        valorX[0]=40+ancho;
        valorY[0]=80;
        
        valorX[1]=40+ancho;
        valorY[1]=80+alto;
        
        valorX[2]=40;
        valorY[2]=80+alto;
        
        valorX[3]=40;
        valorY[3]=80;
        panelDibujo.getGraphics().drawPolygon(valorX, valorY, 4);
        
        valorX[0]=40+ancho;
        valorY[0]=80;
        
        valorX[1]=40+ancho;
        valorY[1]=80+alto;
        
        valorX[2]=40+ancho+profundidad;
        valorY[2]=80-profundidad+alto;
        
        valorX[3]=40+ancho+profundidad;
        valorY[3]=80-profundidad;
        
        panelDibujo.getGraphics().drawPolygon(valorX, valorY, 4);
        
        valorX[0]=40+ancho;
        valorY[0]=80;
        
        valorX[1]=40+ancho+profundidad;
        valorY[1]=80-profundidad;
        
        valorX[2]=40+profundidad;
        valorY[2]=80-profundidad;
        
        valorX[3]=40;
        valorY[3]=80;
        panelDibujo.getGraphics().drawPolygon(valorX, valorY, 4);
    }
    
    private void hacerCubo()
    {
//        panelDibujo.getGraphics()
        int largo=0;
        int ancho=0;
        int espesor=0;
        if(txtLargo.getText().compareTo("")!=0)
        {
            largo=Integer.parseInt(txtLargo.getText());
        }
        
        if(txtAncho.getText().compareTo("")!=0)
        {
            ancho=Integer.parseInt(txtAncho.getText());
        }
        
        if(txtEspesor.getText().compareTo("")!=0)
        {
            espesor=Integer.parseInt(txtEspesor.getText());
        }
        
        dibujar(ancho, largo, espesor);
    }
    
