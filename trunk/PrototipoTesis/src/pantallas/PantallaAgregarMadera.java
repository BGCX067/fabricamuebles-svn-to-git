    private void cargarTabla()
    {
        GestorGrabarLeerDelDisco manejador=new GestorGrabarLeerDelDisco();
        LinkedList listaHerramientas=manejador.obtenerLista(new Madera());
        String nombresColumnas[] = {"Maderas"};
        maderasTablaModel = new DefaultTableModel(nombresColumnas,0);
        Iterator iter = listaHerramientas.iterator();
        while(iter.hasNext())
        {
            Madera aux = (Madera)iter.next();
            String datosFila[] = {aux.getNombre()};
            maderasTablaModel.addRow(datosFila);
            
        }
        tablaMaderas.setModel(maderasTablaModel);
    }
    
    private void cargarMouseListener()
    {
        MouseAdapter adaptadorMouse=new MouseAdapter()
           {@Override
           public void mouseClicked(MouseEvent e) 
              {
                 int fila = tablaMaderas.rowAtPoint(e.getPoint());
                 int columna = tablaMaderas.columnAtPoint(e.getPoint());
                 if ((fila > -1) && (columna > -1))
                 {
                     txtSeleccionado.setText(tablaMaderas.getModel().getValueAt(fila, 0)+"");
//                     herramientaSeleccionada=tablaHerramientas.getModel().getValueAt(fila, 0)+"";
//                     pacienteSeleccionado.setDni(Integer.parseInt(tablaEstructuras.getModel().getValueAt(fila, 0)+""));
//                     pacienteSeleccionado.setApellidoNombre(tablaEstructuras.getModel().getValueAt(fila, 1)+"");
                 }
              }
           };
        
        tablaMaderas.addMouseListener(adaptadorMouse);
    }
    
